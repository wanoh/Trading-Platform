import { createSlice } from '@reduxjs/toolkit'

const initialState = {
  isOpen: false,
  selectedRow: null,
}

const editSliderSlice = createSlice({
  name: 'editSlider',
  initialState,
  reducers: {
    openEditSlider: (state, action) => {
      state.isOpen = true
      state.selectedRow = action.payload
    },
    closeEditSlider: (state) => {
      state.isOpen = false
      state.selectedRow = null
    },
  },
})

export const { openEditSlider, closeEditSlider } = editSliderSlice.actions
export default editSliderSlice.reducer
